CREATE TABLE IF NOT EXISTS geo_audit_tasks (
  task_id CHAR(32) NOT NULL COMMENT '审计任务 ID',
  site_id BIGINT UNSIGNED DEFAULT NULL COMMENT '任务关联的站点 ID',
  domain VARCHAR(255) NOT NULL COMMENT '任务目标域名',
  task_type VARCHAR(64) NOT NULL COMMENT '任务类型，如 site_geo_audit',
  mode VARCHAR(32) NOT NULL DEFAULT 'standard' COMMENT '审计模式，如 standard、premium',
  status VARCHAR(32) NOT NULL COMMENT '任务状态，如 queued、running、completed、failed',
  cache_key CHAR(64) DEFAULT NULL COMMENT '缓存键哈希',
  url VARCHAR(1024) NOT NULL COMMENT '任务原始 URL',
  normalized_url VARCHAR(1024) NOT NULL COMMENT '任务规范化 URL',
  feedback_lang VARCHAR(8) NOT NULL DEFAULT 'en' COMMENT '反馈语言',
  force_refresh TINYINT(1) NOT NULL DEFAULT 0 COMMENT '是否强制刷新缓存与页面快照',
  cached TINYINT(1) NOT NULL DEFAULT 0 COMMENT '是否命中了审计缓存',
  full_audit TINYINT(1) NOT NULL DEFAULT 0 COMMENT '是否开启 full audit',
  requested_max_pages INT NOT NULL DEFAULT 12 COMMENT '请求的最大页面数',
  result_json LONGTEXT DEFAULT NULL COMMENT '任务完成后的完整结果 JSON',
  error_text TEXT DEFAULT NULL COMMENT '任务失败时的错误文本',
  created_at DATETIME(6) NOT NULL COMMENT '任务创建时间',
  updated_at DATETIME(6) NOT NULL COMMENT '任务更新时间',
  completed_at DATETIME(6) DEFAULT NULL COMMENT '任务完成时间',
  PRIMARY KEY (task_id),
  KEY idx_geo_audit_tasks_domain_created (domain, created_at),
  KEY idx_geo_audit_tasks_site_created (site_id, created_at),
  CONSTRAINT fk_geo_audit_tasks_site FOREIGN KEY (site_id) REFERENCES geo_sites (site_id) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='异步审计任务持久化表';
